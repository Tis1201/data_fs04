<script lang="ts">
    import { goto } from "$app/navigation";
    import { toast } from "svelte-sonner";
    import { Button } from "$lib/components/ui/button";
    import { Skeleton } from "$lib/components/ui/skeleton";
    import { ArrowLeft, RefreshCw, CheckCircle, Plus, QrCode, AlertTriangle } from "lucide-svelte";
    import QRCode from "qrcode";
    import PageContainer from "$lib/components/ui_components_sveltekit/layout/PageContainer.svelte";
    import PageHeader from "$lib/components/ui_components_sveltekit/layout/PageHeader.svelte";
    import PageContent from "$lib/components/ui_components_sveltekit/layout/PageContent.svelte";
    import FormCard from "$lib/components/ui_components_sveltekit/form/FormCard.svelte";
    import FormActions from "$lib/components/ui_components_sveltekit/form/FormActions.svelte";
    import AccountForm from "./form.svelte";
    import { whatsAppStore } from "$lib/stores/whatsapp-store";
    import type { ConnectionStatus } from "$lib/stores/whatsapp-store";
    import { Card, CardContent, CardDescription, CardFooter, CardHeader, CardTitle } from "$lib/components/ui/card";
    import type { PageData } from './$types';
    import { writable, derived } from 'svelte/store';
    import { onDestroy } from 'svelte';
    
    // Import the reusable form handler
    import { createFormHandler } from '$lib/components/ui_components_sveltekit/form/utils/formHandler';
    
    export let data: PageData;
    const title = "Create WhatsApp Account";

    // Define breadcrumbs for this page
    const pageCrumbs = [
        ["Admin", "/admin"],
        "Settings",
        ["Whatsapp", "/admin/settings/whatsapp/accounts"],
        "New Account",
    ];
    
    // Track the current step in the account creation process
    let currentStep = 1; // 1: Connect WhatsApp, 2: Account Details, 3: Success
    
    // Account data after creation
    let createdAccount: { id?: string; description: string } | null = null;
    
    // Use the client ID from the server (generated by Baileys)
    let initialClientId = data.clientId || null;
    
    // Create a store for our form-specific state
    const formState = writable<{
        clientId: string | null;
        connectionStatus: ConnectionStatus;
        qrCode: string | null;
        pushName: string | null;
        phoneNumber: string | null;
    }>({
        clientId: initialClientId,
        connectionStatus: 'disconnected',
        qrCode: null,
        pushName: null,
        phoneNumber: null
    });
    
    // Create a readable store for the client ID
    const formClientId = derived(formState, $state => $state.clientId);
    
    // Create a derived store for the form's WhatsApp state
    const formWhatsAppState = derived(formState, $state => ({
        connectionStatus: $state.connectionStatus,
        qrCode: $state.qrCode,
        pushName: $state.pushName,
        phoneNumber: $state.phoneNumber
    }));
    
    // Subscribe to the WhatsApp store to update our form state
    const unsubscribe = whatsAppStore.subscribe($store => {
        // Log for debugging
        console.log('WhatsApp store updated:', {
            storeClientId: $store.clientId,
            formClientId: $formState.clientId,
            storeQrCode: $store.qrCode ? 'present' : 'null',
            storeConnectionStatus: $store.connectionStatus,
            storePushName: $store.pushName,
            storePhoneNumber: $store.phoneNumber
        });
        
        // Update our form state when the store's client ID matches our form's client ID
        formState.update($state => {
            // If the store has our client ID, update our state
            if ($store.clientId === $state.clientId) {
                console.log('Updating form state with client data:', {
                    clientId: $store.clientId,
                    pushName: $store.pushName,
                    phoneNumber: $store.phoneNumber
                });
                
                return {
                    ...$state,
                    connectionStatus: $store.connectionStatus,
                    qrCode: $store.qrCode,
                    pushName: $store.pushName,
                    phoneNumber: $store.phoneNumber
                };
            }
            
            // Otherwise, keep our state as is
            return $state;
        });
    });
    
    // Clean up subscription when component is destroyed
    onDestroy(unsubscribe);
    
    // Log form-specific WhatsApp state changes for debugging
    $: console.log('Form WhatsApp state changed:', {
        connectionStatus: $formWhatsAppState.connectionStatus,
        clientId: $formClientId,
        qrCode: $formWhatsAppState.qrCode ? 'present' : 'null',
        currentStep
    });
    
    // Watch the form-specific WhatsApp state for connection status changes
    $: if ($formWhatsAppState.connectionStatus === 'connected' || $formWhatsAppState.connectionStatus === 'authenticated') {
        console.log('Connected status detected for form client:', $formClientId);
        
        if (currentStep === 1) {
            console.log('Advancing to step 2');
            
            // Force a delay to ensure we have the latest data from the WebSocket
            setTimeout(() => {
                // Get the latest data from the WhatsApp store
                const latestStoreData = whatsAppStore.getState();
                console.log('Latest WhatsApp store data:', latestStoreData);
                
                // Pre-fill the form with the data from the connected client
                const pushName = $formWhatsAppState.pushName || latestStoreData.pushName || 'Unknown';
                const phoneNumber = $formWhatsAppState.phoneNumber || latestStoreData.phoneNumber || '';
                
                console.log('WhatsApp client info for form:', {
                    pushName,
                    phoneNumber,
                    clientId: $formClientId
                });
                
                // Update form values directly
                $form.description = `WhatsApp Account - ${pushName}`;
                $form.client_id = $formClientId;
                $form.name = pushName;
                $form.phoneNumber = phoneNumber;
                
                // Move to account details step
                currentStep = 2;
                toast.success('WhatsApp connected successfully!');
            }, 500); // Short delay to ensure we have the latest data
        }
    }
    
    // Create a form handler with standardized error handling
    const { form, errors, enhance, submitting, constraints, errorMessage } = createFormHandler(data.form, {
        validateOnInput: true,
        debugMode: true,
        action: '?/createAccount',
        onSuccess: (result) => {
            // Only move to success page if we have account data
            if (result.data?.account) {
                createdAccount = {
                    id: result.data.account.id,
                    description: result.data.account.description
                };
                
                // Move to success page
                currentStep = 3;
                toast.success("WhatsApp account created successfully");
            }
        },
        onError: (result) => {
            toast.error(result.form?.message || 'Failed to create WhatsApp account');
        }
    });

    // Function to request a new WhatsApp client (which will trigger a QR code event)
    async function requestNewQRCode() {
        console.log('Requesting new WhatsApp client');
        
        // Reset the WhatsApp store first to clear any errors
        whatsAppStore.reset();
        
        // Show loading toast
        const toastId = toast.loading('Creating new WhatsApp client...');
        
        try {
            // Call the server-side action to create a new client
            // This will trigger Baileys to create a new client and generate a QR code
            const response = await fetch('?/requestQRCode', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json'
                }
            });
            
            const result = await response.json();
            
            if (result.success && result.clientId) {
                // Update our form state with the new client ID from Baileys
                formState.update(state => ({
                    ...state,
                    clientId: result.clientId,
                    connectionStatus: 'disconnected',
                    qrCode: null
                }));
                
                // Dismiss the loading toast and show success
                toast.dismiss(toastId);
                toast.success('New WhatsApp client created. Waiting for QR code...');
            } else {
                // Dismiss the loading toast and show error
                toast.dismiss(toastId);
                toast.error('Failed to create new WhatsApp client');
            }
        } catch (error) {
            console.error('Error creating new WhatsApp client:', error);
            // Dismiss the loading toast and show error
            toast.dismiss(toastId);
            toast.error('Error creating new WhatsApp client');
        }
    }
    
    // Handle error events
    function handleError(event: CustomEvent) {
        console.error('WhatsApp error:', event.detail);
        toast.error(event.detail.message || 'An error occurred');
    }
    
    // Function to check connection status and advance to next step
    function checkConnectionAndAdvance() {
        if ($formWhatsAppState.connectionStatus === 'connected' || $formWhatsAppState.connectionStatus === 'authenticated') {
            // Pre-fill the form with the data from the connected client
            const pushName = $formWhatsAppState.pushName || 'Unknown';
            const phoneNumber = $formWhatsAppState.phoneNumber || '';
            
            // Update form values directly
            $form.description = `WhatsApp Account - ${pushName}`;
            $form.client_id = $formClientId;
            $form.name = pushName;
            $form.phoneNumber = phoneNumber;
            
            // Move to account details step
            currentStep = 2;
            toast.success('WhatsApp connected successfully!');
        } else {
            toast.error('WhatsApp is not connected yet. Please scan the QR code first.');
        }
    }
    
    // Generate QR code from data
    async function generateQRCode(qrCodeData: string) {
        if (!qrCodeData) {
            console.log('No QR code data available');
            return null;
        }
        
        console.log('Generating QR code from data:', qrCodeData.substring(0, 20) + '...');
        
        try {
            // Use QRCode library to generate a data URL
            const url = await QRCode.toDataURL(qrCodeData, {
                errorCorrectionLevel: 'H', // High error correction for better scanning
                margin: 1,
                width: 256,
                color: {
                    dark: '#000000',
                    light: '#FFFFFF'
                }
            });
            
            console.log('QR code generated successfully');
            return url;
        } catch (err) {
            console.error('Error generating QR code:', err);
            throw err;
        }
    }
    
    // Get background color class based on connection status
    function getStatusBgColor(status: ConnectionStatus): string {
        switch (status) {
            case 'awaiting_scan':
                return 'bg-amber-50 border border-amber-200';
            case 'connecting':
                return 'bg-blue-50 border border-blue-200';
            case 'connected':
            case 'authenticated':
                return 'bg-green-50 border border-green-200';
            default:
                return 'bg-red-50 border border-red-200';
        }
    }
</script>

<PageContainer crumbs={pageCrumbs}>
    <PageHeader title={title} />

    <PageContent>
        
        <!-- {$formWhatsAppState.connectionStatus} -->

        {#if currentStep === 1}
            <!-- QR Code Display Step -->
            <FormCard title="Connect WhatsApp" description="Scan the QR code with your WhatsApp app to connect this account.">
                <!-- Simple QR code display -->
                <div class="flex flex-col items-center justify-center space-y-4 p-4">
                    <!-- Connection status display -->
                    <div class="mb-4 p-3 rounded-md w-full flex items-center gap-2 {getStatusBgColor($formWhatsAppState.connectionStatus)}">
                        {#if $formWhatsAppState.connectionStatus === 'awaiting_scan'}
                            <QrCode class="h-5 w-5 text-amber-600" />
                            <div>
                                <p class="text-sm font-medium">Waiting for QR code scan</p>
                                <p class="text-xs text-muted-foreground">Please scan the QR code with your WhatsApp app</p>
                            </div>
                        {:else if $formWhatsAppState.connectionStatus === 'connecting'}
                            <RefreshCw class="h-5 w-5 text-blue-600 animate-spin" />
                            <div>
                                <p class="text-sm font-medium">Connecting</p>
                                <p class="text-xs text-muted-foreground">Establishing connection to WhatsApp...</p>
                            </div>
                        {:else if $formWhatsAppState.connectionStatus === 'connected'}
                            <CheckCircle class="h-5 w-5 text-green-600" />
                            <div>
                                <p class="text-sm font-medium">Connected</p>
                                <p class="text-xs text-muted-foreground">WhatsApp connection established</p>
                            </div>
                        {:else if $formWhatsAppState.connectionStatus === 'authenticated'}
                            <CheckCircle class="h-5 w-5 text-green-600" />
                            <div>
                                <p class="text-sm font-medium">Authenticated</p>
                                <p class="text-xs text-muted-foreground">Successfully authenticated with WhatsApp</p>
                            </div>
                        {:else}
                            <AlertTriangle class="h-5 w-5 text-red-600" />
                            <div>
                                <p class="text-sm font-medium">Disconnected</p>
                                <p class="text-xs text-muted-foreground">Not connected to WhatsApp</p>
                            </div>
                        {/if}
                    </div>
                    
                    {#if $formWhatsAppState.qrCode}
                        <!-- Show QR code when available -->
                        <div class="w-64 h-64 border border-border rounded-md overflow-hidden flex items-center justify-center bg-white">
                            {#await generateQRCode($formWhatsAppState.qrCode)}
                                <!-- Loading state while generating QR code -->
                                <Skeleton class="h-64 w-64" />
                            {:then qrCodeUrl}
                                <!-- QR code image -->
                                <img 
                                    src={qrCodeUrl} 
                                    alt="WhatsApp QR Code" 
                                    class="w-full h-full object-contain"
                                />
                            {:catch error}
                                <!-- Error state -->
                                <div class="text-center text-destructive">
                                    <AlertTriangle class="h-12 w-12 mx-auto mb-2" />
                                    <p class="text-sm">Failed to generate QR code</p>
                                </div>
                            {/await}
                        </div>
                        <p class="text-sm text-center text-muted-foreground">
                            Scan this QR code with your WhatsApp app to connect
                        </p>
                    {:else if $formWhatsAppState.connectionStatus === 'connecting'}
                        <!-- Loading state when connecting -->
                        <div class="w-64 h-64 flex items-center justify-center">
                            <Skeleton class="h-64 w-64" />
                        </div>
                        <Skeleton class="h-4 w-48" />
                    {:else}
                        <!-- No QR code available state -->
                        <div class="w-64 h-64 border-2 border-dashed border-muted-foreground rounded-md flex items-center justify-center">
                            <div class="text-center">
                                <QrCode class="h-12 w-12 mx-auto mb-2 text-muted-foreground" />
                                <p class="text-sm text-muted-foreground">QR code not available</p>
                            </div>
                        </div>
                        <p class="text-sm text-center text-muted-foreground">
                            Click the button below to request a QR code
                        </p>
                    {/if}
                </div>
                    
                <div class="mt-4 flex flex-col gap-2">
                    <!-- Request new QR code button -->
                    <!-- <Button 
                        variant="outline" 
                        size="sm"
                        on:click={requestNewQRCode}
                    >
                        <RefreshCw class="h-4 w-4 mr-2" />
                        Request New QR Code
                    </Button> -->
                    
                    <!-- Manual next step button as fallback -->
                    {#if $formWhatsAppState.connectionStatus === 'connected' || $formWhatsAppState.connectionStatus === 'authenticated'}
                        <Button 
                            variant="default"
                            on:click={() => currentStep = 2}
                        >
                            <CheckCircle class="h-4 w-4 mr-2" />
                            Continue to Account Details
                        </Button>
                    {/if}
                </div>
            </FormCard>
        {:else if currentStep === 2}
            <!-- Step 2: Account Details - Using the extracted form component -->
            <AccountForm 
                form={$form}
                errors={$errors}
                enhance={enhance}
                submitting={$submitting}
                constraints={$constraints}
                errorMessage={$errorMessage}
                on:goBack={() => currentStep = 1}
            />
        {:else if currentStep === 3}
            <!-- Step 3: Success Page -->
            <FormCard title="Account Created Successfully" description="Your WhatsApp account has been created successfully.">
                <div class="space-y-6">
                    
                    <div class="bg-muted/40 p-4 rounded-lg border border-muted">
                        <h4 class="text-sm font-medium mb-2">Account Details</h4>
                        <div class="space-y-2">
                            <CheckCircle class="h-6 w-6 text-green-500" />
                            <div>

                                <p class="text-xs text-muted-foreground">Description</p>
                                {#if createdAccount?.description}
                                    <p class="text-sm">{createdAccount.description}</p>
                                {:else}
                                    <Skeleton class="h-4 w-48" />
                                {/if}
                            </div>
                        </div>
                    </div>

                    <FormActions>
                        <Button 
                            variant="outline" 
                            on:click={() => goto('/admin/settings/whatsapp/accounts')}
                        >
                            <ArrowLeft class="h-4 w-4 mr-2" />
                            Back to Accounts
                        </Button>
                    </FormActions>
                </div>
            </FormCard>
        {/if}
    </PageContent>
</PageContainer>
